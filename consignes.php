	<p class="h3 d-flex justify-content-center text-danger my-3">ICI LES CONSIGNES DU JEU</p>
	<P>
		Le but du jeu est de trouver un mot en tapant une lettre à chaque fois. Si la lettre est dans le mot secret, alors les lettres trouvées dans le mot se dévoilent. <br/>
		Comme indice, vous aurez le nombre de caractères du mot secret dès le début d'une partie. <br/>
		<strong class="text-info d-inline-block my-3"><span class="text-warning">REMARQUE :</span> Le mot secret est généré au hazard parmi ceux définis.</strong>
